USE bamazonDB;

CREATE TABLE products(
    id INT NOT NULL AUTO_INCREMENT,
    product_name VARCHAR(50) NULL,
    department_name VARCHAR (30) NULL,
    product_description VARCHAR (250) NOT NULL,
    price DECIMAL (10,2) INT NULL,
    stock_quantity INTEGER, 
  PRIMARY KEY (id)
);

